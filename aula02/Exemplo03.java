public class Exemplo03 {
    public static void main(String[] args) {
        int i = 1;

        while (i < 5) {
            System.out.println(i);
            i++;
        }

        i = 8;

        for(int a=1; a < 5; a++) {
            System.out.println(a);
        }

        // a = 9; não pode ser usado fora do laço
    }
}
